    public void llenartabla(){
        try{
            Statement stmt=cn.createStatement();
            stmt.execute("select*from Paciente");
            ResultSet rs=stmt.getResultSet();
            Object  O[]=new Object[5];
            m.setRowCount(0);
            if(rs!=null)
            {
                while(rs.next()){
                    O[0]=rs.getInt("Id");
                    O[1]=rs.getString("Nombre");
                    O[2]=rs.getInt("Edad");
                    O[3]=rs.getLong("Telefono");
                    O[4]=rs.getString("Diagnostico");
                    m.addRow(O);
                }
            }else{showMessageDialog(this,"Error");}
            stmt.close();
        }catch(SQLException e){
            showMessageDialog(this,e.getMessage());
        }
    }
