    private void txtBuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyTyped
        char e=evt.getKeyChar();
        if((((e<'a')||(e>'z'))&&((e<'A')||(e>'Z')))&&(e!=' ')){
            evt.consume();
            lblLetras.setText("¡Solo letras!");
        }else{
            String cad="select*from Paciente";
            lblLetras.setText("");
        try{
            Statement stmt=cn.createStatement();
            cad=cad+" where nombre like'"+txtBuscar.getText()+"*'";             
            stmt.execute(cad);
            m.setRowCount(0);
            ResultSet rs=stmt.getResultSet();
            Object  O[]=new Object[5];
            if(rs!=null)
            {
                while(rs.next()){
                    O[0]=rs.getInt("Id");
                    O[1]=rs.getString("Nombre");
                    O[2]=rs.getInt("Edad");
                    O[3]=rs.getLong("Telefono");
                    O[4]=rs.getString("Diagnostico");
                    m.addRow(O);
                }
            }else{showMessageDialog(this,"Error");}
            stmt.close();
        }catch(SQLException ex){showMessageDialog(this,"Error de busqueda \n"+ex.getMessage());}
        }
    }//GEN-LAST:event_txtBuscarKeyTyped

