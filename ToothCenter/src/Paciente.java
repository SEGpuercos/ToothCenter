    private void tblPacienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPacienteMouseClicked
        if(tblPaciente.getSelectedRow()<0){
            return;
        } 
        NuevaCita obj= new NuevaCita();
        int c=tblPaciente.getSelectedRow();
        obj.id=Integer.parseInt(m.getValueAt(c, 0).toString());
        obj.nombre=m.getValueAt(c, 1).toString();
        obj.edad=Integer.parseInt(m.getValueAt(c, 2).toString());
        obj.telefono=Long.parseLong(m.getValueAt(c, 3).toString());
        obj.diagnostico=m.getValueAt(c, 4).toString();
        obj.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_tblPacienteMouseClicked

